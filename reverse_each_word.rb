def reverse_each_word = ["Hello there, and how are you?"]
  reverse_each_word.each do |reverse_each_word|
     puts #{reverse_each_word.reverse} 
end

def reverse_each_word = ["Hello there, and how are you?"]
  reverse_each_word.collect do |reverse_each_word|
    puts #{reverse_each_word.collect}
end
